package com.ruoyi.domain;

public class InterFaceStockData {
    private String 产品编码;
    private String 产品硬件版本;
    private String 产品软件版本;
    private String 库存;

    public String get产品编码() {
        return 产品编码;
    }

    public void set产品编码(String 产品编码) {
        this.产品编码 = 产品编码;
    }

    public String get产品硬件版本() {
        return 产品硬件版本;
    }

    public void set产品硬件版本(String 产品硬件版本) {
        this.产品硬件版本 = 产品硬件版本;
    }

    public String get产品软件版本() {
        return 产品软件版本;
    }

    public void set产品软件版本(String 产品软件版本) {
        this.产品软件版本 = 产品软件版本;
    }

    public String get库存() {
        return 库存;
    }

    public void set库存(String 库存) {
        this.库存 = 库存;
    }

    public String get库存时间() {
        return 库存时间;
    }

    public void set库存时间(String 库存时间) {
        this.库存时间 = 库存时间;
    }

    private String 库存时间;
}
