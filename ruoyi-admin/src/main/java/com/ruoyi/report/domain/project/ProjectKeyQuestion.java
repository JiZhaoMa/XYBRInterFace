public class ProjectKeyQuestion {
    private String projectCode;
    private String projectName;
    private String keyQustion;
    private String isComSource;

    public String getProjectCode() {
        return projectCode;
    }

    public void setProjectCode(String projectCode) {
        this.projectCode = projectCode;
    }

    public String getProjectName() {
        return projectName;
    }

    public void setProjectName(String projectName) {
        this.projectName = projectName;
    }

    public String getKeyQustion() {
        return keyQustion;
    }

    public void setKeyQustion(String keyQustion) {
        this.keyQustion = keyQustion;
    }

    public String getIsComSource() {
        return isComSource;
    }

    public void setIsComSource(String isComSource) {
        this.isComSource = isComSource;
    }
}
