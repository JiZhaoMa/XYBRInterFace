package com.ruoyi.report.domain.cockpit;

public class Patent {
    private String patentName;
    private String patentPerson;
    private String patentDate;

    public String getPatentName() {
        return patentName;
    }

    public void setPatentName(String patentName) {
        this.patentName = patentName;
    }

    public String getPatentPerson() {
        return patentPerson;
    }

    public void setPatentPerson(String patentPerson) {
        this.patentPerson = patentPerson;
    }

    public String getPatentDate() {
        return patentDate;
    }

    public void setPatentDate(String patentDate) {
        this.patentDate = patentDate;
    }
}
